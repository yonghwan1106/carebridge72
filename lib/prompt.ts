import type { IntakeRequest } from "@/lib/types";

const SCHEMA = `{
  "structured": {
    "targetType": "예: 17세 최중증 발달장애인",
    "disabilityFeatures": ["의사소통 제한", "야간 불안", "..."],
    "gapReason": "예: 주 보호자 응급 입원",
    "estimatedGapHours": "예: 약 12시간 이상 (야간~익일 오전)",
    "location": "예: 성남시 분당구 정자동",
    "currentServices": ["장애인활동지원"],
    "riskFactors": ["단독 체류", "자해·불안", "복약 누락"],
    "missingQuestions": ["담당자가 추가로 확인할 질문 (최대 4개)"]
  },
  "urgency": {
    "level": "즉시|당일|72시간|일반",
    "score": 0-100 정수,
    "rationale": "분류 근거 1~2문장",
    "safetyFlags": ["규칙 기반 안전 점검 항목 (최대 4개)"]
  }
}`;

export function buildStructurePrompt(req: IntakeRequest): { system: string; user: string } {
  const system = [
    "당신은 '케어브릿지 72'의 접수 분석기입니다. 최중증 발달장애인 등 상시 돌봄이 필요한 가정에서 보호자 공백이 발생한 상황을,",
    "지자체 복지 담당자가 바로 쓸 수 있도록 표준 항목으로 '구조화'하고 '긴급도'를 분류합니다.",
    "",
    "원칙:",
    "- 당신은 기관 배정·수급 결정을 하지 않습니다. 상황 구조화와 긴급도 분류만 수행합니다.",
    "- 안전(자해·복약·단독 체류·야간)을 우선 점검하고 safetyFlags 로 표시합니다.",
    "- 입력에 없는 사실을 지어내지 말고, 불확실하면 missingQuestions 로 남깁니다.",
    "",
    "긴급도 기준:",
    "- 즉시: 단독 체류 + 야간/자해 등 즉시 안전 위험",
    "- 당일: 오늘 안에 임시 돌봄 연결이 필요",
    "- 72시간: 72시간 내 제도·기관 연계 필요",
    "- 일반: 정보 안내·일반 상담 수준",
    "",
    "출력 규칙:",
    "- 아래 JSON 스키마와 동일한 구조의 '유효한 JSON 하나만' 출력. 코드펜스(```)나 설명 문장 금지.",
    "- 모든 텍스트는 한국어. 배열 항목은 간결하게.",
    "",
    "JSON 스키마:",
    SCHEMA,
  ].join("\n");

  const user = [
    "## 접수된 돌봄공백 상황",
    `거주지(추정): ${req.region || "미상"}`,
    req.occurredAt ? `발생 시각: ${req.occurredAt}` : "",
    "",
    req.text,
    "",
    "위 상황을 분석하여 스키마에 맞는 JSON 하나만 출력하십시오.",
  ]
    .filter((l) => l !== "")
    .join("\n");

  return { system, user };
}
